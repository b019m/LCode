package lecturecode.week0;

public class StringEscapeCharacters {
    public static void main(String[] args) {

        // declares a string variable with escape characters
        String message = "This class is \"Java Programming\"!";
        System.out.println(message);

        // declares a string variable with newline escape characters
        String anotherMessage = "Welcome, everyone!\n I hope you enjoy the class";
        System.out.println(anotherMessage);

        // declares a string variable with tab escape
        String andAnotherMessage = "Videos\tLabs\tZoom";
        System.out.println(andAnotherMessage);

    }
}
