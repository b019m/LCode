package lecturecode.week2;

public class ForLoops {
    public static void main(String[] args) {
        // loop to count from 0 to 4
        for (int x=0; x < 5; x++) {
            // print the current calue of x plus 1
            System.out.println(x + 1);
        }

        for (int x =10; x >=0; x--) { // loop to count down from 10 to 0
            System.out.println(x + "   "); // print the current value of x followed by spaces
        }
    }
}
